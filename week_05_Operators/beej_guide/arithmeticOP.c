#include <stdio.h>

int main() {

    int i = 0;

    i = i + 3; // Addition (+) and assigment (=) operators, add 3 to i
    i = i - 8; // Subtraction
    i = i * 9; // Multiplication
    i = i / 2; // Division
    i = i % 5; // Modulo

    i += 3; // some as i = i + 3
    i -= 8; // some as i = i - 8
    i *= 9; // some as i = i * 9
    i /= 2; // some as i = i / 2
    i %= 5; // some as i = i % 5


    return (0);
}