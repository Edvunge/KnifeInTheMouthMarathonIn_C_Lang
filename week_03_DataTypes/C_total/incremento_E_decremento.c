#include <stdio.h>

/*
C inclui dois operadores úteis geralmente não encontrados em outras linguagens.
são os operadores de incremento e decremento, ++ e -- 
O operador ++ soma 1 ao seu operando, e -- subtrai 1 em outras palavras:
*/

int main() {

    int x = x + 1;

    // é o mesmo que 

    ++x;

    // e

    x = x - 1;

    // é o mesmo que 

    x--;
    return (0);
}

