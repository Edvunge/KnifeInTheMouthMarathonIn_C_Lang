#include <stdio.h> // this line tells the compiler to includes the contents of the standard library header file

int main(void) { // this line starts the definition of a principal function (main)
                 // the type and number of arguments it expects(void, meaning none)
                 // and the type of value that this function returns (int)
                 // program execution starts in the main() function

    puts("Hello, World!"); // this line calls the puts() function to output text to standard output

    return (0); // meaning it needs to return an integer
} // the curly braces are used in pairs to indicate where a block of code begins and ends